<?php
namespace App\Controller;

use Symfony\Component\HttpFoundation\Request;
use Symfony\Component\HttpFoundation\JsonResponse;


class ApiController
{


    /**
     * @var integer HTTP status code - 200 (OK) by default
     */
    protected $statusCode = 200;

    /**
     * Gets the value of statusCode.
     *
     * @return integer
     */
    public function getStatusCode()
    {
        return $this->statusCode;
    }

    /**
     * Sets the value of statusCode.
     *
     * @param integer $statusCode the status code
     *
     * @return self
     */
    protected function setStatusCode($statusCode)
    {
        $this->statusCode = $statusCode;

        return $this;
    }

    /**
     * Returns a JSON response
     *
     * @param array $data
     * @param array $headers
     *
     * @return Symfony\Component\HttpFoundation\JsonResponse
     */
    public function respond($data, $headers = [])
    {
        return new JsonResponse($data, $this->getStatusCode(), $headers);
    }

    /**
     * Sets an error message and returns a JSON response
     *
     * @param string $errors
     *
     * @return Symfony\Component\HttpFoundation\JsonResponse
     */
    public function respondWithErrors($errors, $headers = [])
    {
        $data = [
            'errors' => $errors,
        ];

        return new JsonResponse($data, $this->getStatusCode(), $headers);
    }

    /**
     * Returns a 401 Unauthorized http response
     *
     * @param string $message
     *
     * @return Symfony\Component\HttpFoundation\JsonResponse
     */
    public function respondUnauthorized($message = 'Not authorized!')
    {
        return $this->setStatusCode(401)->respondWithErrors($message);
    }

    /** 
     * Returns a 422 Unprocessable Entity
     * @param string $message
     * 
     * @return Symfony\Component\HttpFoundation\JsonResponse
     * 
     */

     public function respondValidationError($message = 'Validation errors')
     {
         return $this->setStatusCode(422)->respondWithErrors($message);
     }


     /** Returns a 404 Not Found
      * 
      * @param string $message
      * @return Symfony\component\HttpFoundation\JsonResponse
      */

      public function respondNotFound($message = 'Not Found')
      {
          return $this->setStatusCode(404)->respondWithErrors($message);
      }

      /** 
       * Returns a 201 Created
       * @param array $data
       * 
       * @return Symfony\Component\HttpFoundation\JsonResponse
       */

       public function resondCreated($data = [])
       {
            return $this->setStatusCode(201)->respondWithErrors($data);
       }

       protected function transformJsonBody(\Symfony\Component\HttpFoundation\Request $request)
       {
           $data = json_decode($request->getContent(), true);
           if (json_last_error() !== JSON_ERROR_NONE) {
               return null;
           }
           if ($data === null) {
               return $request;
           }
           $request->request->replace($data);
           return $request;
       }

           /** 
    * Attempt authorization using jwt-verifier
    *
    * @return bool
    */

    public function isAuthorized(): bool
    {
        if (! isset( $_SERVER['HTTP_AUTHORIZATION'])) {
            return false;
        }
        $authType = null;
        $authData = null;
        // Extract the auth type and the data from the Authorization header.
        @list($authType, $authData) = explode(" ", $_SERVER['HTTP_AUTHORIZATION'], 2);
        // If the Authorization Header is not a bearer type, return a 401.
        if ($authType != 'Bearer') {
            return false;
        }
        // Attempt authorization with the provided token
        try {
            $jwtVerifier = (new \Okta\JwtVerifier\JwtVerifierBuilder())
                ->setAdaptor(new \Okta\JwtVerifier\Adaptors\SpomkyLabsJose())
                ->setAudience('api://default')
                ->setClientId('0oahduoszgZqFMX5W0h7')
                ->setIssuer('https://dev-408002.oktapreview.com/oauth2/default')
                ->build();

            $jwt = $jwtVerifier->verify($authData);
        } catch (\Exception $e) {
            return false;
        }

        return true;
    }

}
